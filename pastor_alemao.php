<?php
require_once "cao.php";

class Pastor_alemao extends Cao{
    private $nome;


    /**
     * Get the value of nome
     */ 
    public function getNome()
    {
        return $this->nome;
    }

    /**
     * Set the value of nome
     *
     * @return  self
